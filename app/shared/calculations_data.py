def _build_name_number_map() -> dict[str, int]:
    """Создает словарь маппинга букв на числовые значения."""
    mapping = {
        1: ["A", "J", "S", "А", "И", "С", "Ъ", "Ь"],
        2: ["B", "K", "T", "Б", "Й", "Т", "Ы"],
        3: ["C", "L", "U", "В", "К", "У", "Э"],
        4: ["D", "M", "V", "Г", "Л", "Ф", "Ю"],
        5: ["E", "N", "W", "Д", "М", "Х", "Я"],
        6: ["F", "O", "X", "Е", "Ё", "Н", "Ц"],
        7: ["G", "P", "Y", "Ж", "О", "Ч", "Щ"],
        8: ["H", "Q", "Z", "З", "П", "Ш"],
        9: ["I", "R", "Р"],
    }
    result: dict[str, int] = {}
    for value, letters in mapping.items():
        for letter in letters:
            result[letter.upper()] = value
            result[letter.lower()] = value
    return result


