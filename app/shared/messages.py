def _format_history_preview(history: Iterable[dict[str, str | int | None]], current_text: str, limit: int) -> str | None:
    preview: list[str] = []
    for entry in reversed(list(history)):
        if not entry:
            continue
        text = entry.get("text") if isinstance(entry, dict) else None
        if not text or text == current_text:
            continue
        preview.append(f"â€¢ {text}")
        if len(preview) >= limit:
            break
    if not preview:
        return None
    preview.reverse()
    return "ğŸ“š ĞĞµĞ´Ğ°Ğ²Ğ½Ğ¸Ğµ Ğ°Ñ„Ñ„Ğ¸Ñ€Ğ¼Ğ°Ñ†Ğ¸Ğ¸:\n" + "\n".join(preview)


